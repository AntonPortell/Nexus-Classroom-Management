    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String Query = "select * from APP.CLASS_INFO where INDEX='"+jTextField1.getText()+"'";
        try{
            Con = DriverManager.getConnection(databaseURL);
            St = Con.createStatement();
            Rs = St.executeQuery(Query);

            if (Rs.next())
            {
                String index = Rs.getString("INDEX");
                String teacher = Rs.getString("TEACHER");
                String subject = Rs.getString("SUBJECT");
                Boolean type = Rs.getBoolean("TYPE");
                
                jLabel3.setText("Class - " + index);
                jLabel4.setText("Teacher - " + teacher);
                jLabel5.setText("Subject - " + subject);
                if (type){
                    jLabel6.setText("Type - Open");
                } else {
                    jLabel6.setText("Type - Closed");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Message: No Class found");
            }
            }catch(Exception e)
            {
            }        
    }//GEN-LAST:event_jButton1ActionPerformed

